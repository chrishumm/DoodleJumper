bool AppDelegate::applicationDidFinishLaunching() {
    // initialize director
	srand( (unsigned)time( NULL ) ); //seeds our random
    CCDirector* pDirector = CCDirector::sharedDirector();
    CCEGLView* pEGLView = CCEGLView::sharedOpenGLView();

    pDirector->setOpenGLView(pEGLView);
    // turn on display FPS
    pDirector->setDisplayStats(true);

    // set FPS. the default value is 1.0/60 if you don't call this
    pDirector->setAnimationInterval(1.0 / 60);
	//	eglView->setDesignResolutionSize(480, 320, kResolutionExactFit);
    // create a scene. it's an autorelease object
	pEGLView->setDesignResolutionSize(800, 600, kResolutionShowAll); //fits our resolution to our 
    CCScene *pScene = menuScene::create();

    // run
    pDirector->runWithScene(pScene);

    return true;
}

